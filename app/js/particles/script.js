//Spoiler=========================================================================
$('.spoiler').click(function(){
  $(this).toggleClass('opened').toggleClass('closed').prev().slideToggle(700);
  if($(this).hasClass('opened')) {
    $(this).find('label').html('Згорнути текст');
  }
  else {
    $(this).find('label').html('Читати далі');
  }
});
